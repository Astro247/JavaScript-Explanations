// Gli operatori matematici in javascript funzionano esattamente come gli operatori matematici in python.

let number_one = 5, number_two = 5;

console.log(`Sum of two numbers: ${number_one + number_two}`)

console.log(`Difference of two numbers: ${number_one - number_two}`)

console.log(`Product of two numbers: ${number_one * number_two}`)

console.log(`Ratio of two numbers: ${number_one / number_two}`)

console.log(`Power of two numbers: ${number_one ** number_two}`)

console.log(`Module of two numbers: ${number_one % number_two}`)